import { AppErrorCode } from '@bridge-monorepo/shared';

/**
 * Словарь человекочитаемых сообщений об ошибках на русском языке.
 * Жестко завязан на AppErrorCode из shared.
 */
export const ERROR_MESSAGES: Record<AppErrorCode, string> = {
  // Валидация ввода (400)
  NOTE_EMPTY: 'Пожалуйста, добавьте текст заметки.',
  NOTE_TOO_LONG: 'Текст заметки слишком длинный.',
  INVALID_JSON_FORMAT: 'Не удалось обработать данные. Попробуйте обновить страницу или проверить соединение.',
  VALIDATION_ERROR: 'Проверьте правильность заполнения полей и попробуйте снова.',

  // НОВЫЕ СТРОГИЕ ОШИБКИ ИЗ API V2
  TITLE_EMPTY: 'Заголовок заметки обязателен. Пожалуйста, введите название.',
  TITLE_TOO_LONG: 'Заголовок слишком длинный. Максимально 100 символов.',
  CONTENT_TOO_LONG: 'Превышен лимит символов в тексте заметки. Сократите текст.',
  INVALID_ID_FORMAT: 'Идентификатор заметки имеет неверный формат. Проверьте ссылку или обновите страницу.',

  DUPLICATE_ID:
    'Заметка с таким идентификатором уже существует. Используйте другой ID или обновите существующую заметку.',
  // Ошибки ресурсов и сети (404, 413, 500)
  NOTE_NOT_FOUND: 'Заметка не найдена. Возможно, она была удалена или ссылка устарела.',
  PAYLOAD_TOO_LARGE: 'Файл или данные слишком большие. Уменьшите размер запроса.',
  INTERNAL_SERVER_ERROR: 'На сервере произошла непредвиденная ошибка. Мы уже работаем над её исправлением.',
};

/**
 * Безопасное получение текста ошибки по её строгому коду
 */
export function getErrorMessage(code: AppErrorCode | null | undefined): string {
  if (!code) return '';
  return ERROR_MESSAGES[code] || 'Произошла непредвиденная ошибка.';
}
